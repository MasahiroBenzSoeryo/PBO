package pertemuan1;

public class latihan1_poinE2a {

	public static void main(String[] args) {

		double pi = 3.14;
		double diameterLingkaran = 10;
		double jariJariLingkaran = diameterLingkaran / 2;
		double kelilingLingkaran = 2 * pi * jariJariLingkaran;
        
        System.out.println("Diameter lingkaran = 10");
        System.out.println("Keliling lingkaran dengan diameter 10 adalah = " + kelilingLingkaran);

	}

}
